package week2_tasks;

public class Zaid_1 {
    public static void main(String[] args) {

        // swap two variable values without a third variable
        // task 1

        int a = 10;
        int b = 20;

        a = a + b; // 10 + 20 = 30 for a
        b = a - b; // 30 - 20 = 10 for b
        a = a - b; // 30 - 10 = 20 for a

        System.out.println("a = " + a);
        System.out.println("b = " + b);


    }
}
